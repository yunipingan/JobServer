package cn.pingweb.model;

public class Seeker {
	private String openid;	
	private String name;
	private String birthday;
	private String sex;
	private String telephone;
	private String email;
	private String avatarUrl;
	private String city;
	private String hope_job;
	private String degree;
	private String electronic_resume;
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAvatarUrl() {
		return avatarUrl;
	}
	public void setAvatarUrl(String avatarUrl) {
		this.avatarUrl = avatarUrl;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getElectronic_resume() {
		return electronic_resume;
	}
	public void setElectronic_resume(String electronic_resume) {
		this.electronic_resume = electronic_resume;
	}
	public String getHope_job() {
		return hope_job;
	}
	public void setHope_job(String hope_job) {
		this.hope_job = hope_job;
	}
	
}
